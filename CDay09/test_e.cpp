// (b) 什么叫空指针？C 语言是如何表示空指针的？

// 1. 空指针的定义与用途
// 定义：空指针是一个 明确不指向任何对象或函数 的指针。

// 用途：

// 初始化指针变量，防止野指针。

// 表示内存分配失败（如 malloc 返回 NULL）。

// 作为函数返回值，指示错误或结束条件。

// 2. C语言中空指针的表示
// 在C语言中，空指针通过 NULL 宏 表示，其标准定义如下：

// c
// #define NULL ((void*)0)  // 通常定义在 <stddef.h> 或 <stdio.h> 中
// 本质：NULL 是一个值为 0 的指针常量，类型为 void*。

// 赋值示例：

// c
// int *p = NULL;  // p是一个空指针
// 3. 空指针的底层实现
// 内存地址：空指针的值通常对应内存地址 0x0（具体实现可能因系统而异）。

// 访问限制：大多数操作系统禁止用户程序访问地址 0x0，因此解引用空指针会触发 段错误（Segmentation Fault）。

// c
// int *p = NULL;
// *p = 10;  // 运行时崩溃：试图向地址0写入数据
// 4. 空指针的检测
// 通过条件判断检查指针是否为空：

// c
// int *p = malloc(sizeof(int));
// if (p == NULL) {
//     // 处理内存分配失败
// } else {
//     // 安全使用指针
//     *p = 42;
//     free(p);
// }
// 5. 空指针与未初始化指针的区别
// 空指针	未初始化指针
// 显式赋值为 NULL（如 int *p = NULL;）	未赋值，指向随机内存地址（野指针）
// 安全可控	危险，解引用会导致未定义行为
