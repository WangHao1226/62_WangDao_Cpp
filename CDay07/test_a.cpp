// (a) 什么是指针，什么是指针变量？
// 1. 指针的本质
// 指针：本质是一个内存地址，表示数据在内存中的位置。

// 每个变量存储在内存的某个位置，该位置的编号即地址。

// 指针的作用：

// 通过地址直接访问或操作内存中的数据。

// 支持动态内存分配、数组操作、函数传参等高级功能。

// 2. 指针变量的定义
// 指针变量：专门用于存储指针（内存地址）的变量。

// 声明格式：数据类型 *变量名;

// 3. 指针与指针变量的区别
// 特性	指针	指针变量
// 本质	内存地址（数值）	存储地址的变量
// 可变性	固定（由系统分配）	可修改（指向不同地址）
// 内存占用	无（仅地址值）	有（需内存空间存储地址）
// 操作	通过地址访问数据	可赋值、运算、传递

