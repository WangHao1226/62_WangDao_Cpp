// (d) 如何给指针变量赋值？
// 一、指针赋值的正确方式
// 1. 指向变量的地址（使用 & 操作符）
// c
// int num = 10;
// int *p = &num;  // 正确：p指向num的地址
// 2. 指向动态分配的内存（malloc/calloc）
// c
// int *arr = malloc(5 * sizeof(int));  // 动态数组
// if (arr != NULL) {                   // 必须检查分配是否成功
//     arr[0] = 1;                      // 安全操作
// }
// 3. 指向其他指针的值（指针间赋值）
// c
// int a = 10;
// int *p1 = &a;
// int *p2 = p1;    // 正确：p2和p1指向同一地址
// 4. 指向数组首地址
// c
// int arr[5] = {1, 2, 3};
// int *p = arr;     // 正确：arr自动转换为首元素地址
// 5. 初始化为空指针（NULL）
// c
// int *p = NULL;    // 安全初始化
// if (p != NULL) {  // 使用前检查
//     *p = 10;
// }
