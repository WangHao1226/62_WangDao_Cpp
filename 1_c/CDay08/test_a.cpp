// (a) char str[] = "abcd"和char *p = "abcd"有什么区别？
// 1. 内存分配与存储位置
// char str[] = "abcd"
// 这是一个字符数组，会在内存中分配连续的空间（栈或静态存储区，取决于作用域）。数组的大小由字符串字面量决定（包含结尾的 '\0'），因此 str 的长度为5字节（'a', 'b', 'c', 'd', '\0'）。
// 特点：数据存储在数组的独立内存空间中，初始化时会复制字符串内容到数组中。

// char *p = "abcd"
// 这是一个指向字符串常量的指针。字符串 "abcd" 存储在程序的只读段（如 .rodata），p 本身是一个指针变量（存储该字符串的地址）。若 p 是局部变量，则指针本身存储在栈中。
// 特点：直接指向字符串常量，不复制内容，且内容不可修改。

// 2. 可修改性
// 数组 str
// 可以修改数组内容，例如 str[0] = 'e' 是合法的。

// 指针 p
// 指向的是字符串常量，尝试修改内容（如 p[0] = 'e'）会导致未定义行为（通常引发段错误）。

// 3. sizeof 运算符的结果
// sizeof(str)
// 返回数组的总大小（5字节）。

// sizeof(p)
// 返回指针的大小（4字节或8字节，取决于系统架构）。

// 4. 指针运算
// 数组名 str
// 是常量指针，不能进行自增操作（如 str++ 非法）。

// 指针 p
// 是变量，可以重新赋值或自增（如 p++ 合法）。

// 5. 赋值灵活性
// 数组 str
// 不能直接赋值，如 str = "new" 非法。

// 指针 p
// 可以重新指向其他字符串，如 p = "another" 合法。