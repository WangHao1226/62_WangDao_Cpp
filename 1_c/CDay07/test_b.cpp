// (b) 和指针相关的两个基本操作是什么？它们分别表示什么含义？
// 1. 取地址操作符 &
// 含义：
// 获取变量在内存中的地址。

// 变量存储在内存的某个位置，& 返回该位置的起始地址。

// 结果是一个指针（内存地址）。

// 语法：

// c
// &变量名

// 关键点：

// 只能用于已定义的变量（如 &a），不能用于常量（如 &10 非法）。

// 数组名本身可视为地址，无需使用 &（如 int arr[5]; 中 arr 等价于 &arr[0]）。

// 2. 解引用操作符 *
// 含义：
// 通过指针访问或修改其指向的内存地址中的数据。

// 根据指针存储的地址，找到对应的内存位置并操作其内容。

// 语法：

// c
// *指针变量名

// 关键点：

// 必须确保指针已初始化并指向有效内存（否则解引用野指针会导致未定义行为）。

// 可用于读写操作（如 *p = 20）。

// 在指针声明中，* 表示变量是指针类型（如 int *p）。

// 3. 操作符对比
// 操作符	作用	适用场景	示例
// &	获取变量的内存地址	初始化指针变量、传递地址	int *p = &a;
// *	访问指针指向的内存数据	读写目标数据、指针解引用	int value = *p;
