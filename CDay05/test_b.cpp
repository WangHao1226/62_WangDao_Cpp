// 1. 二维数组的本质
// 元素数量：二维数组 int arr[M][N] 在内存中本质是一个一维数组，该一维数组共有 M 个元素。

// 元素类型：每个元素的类型是 int[N]（即长度为 N 的一维数组）。
// 例如，arr[i] 的类型是 int[N]，表示第 i 行的所有元素。

// arr[i][j] 的地址计算：
// 二维数组按行优先顺序连续存储，因此 arr[i][j] 的地址为：

// c
// &arr[0][0] + i * N * sizeof(int) + j * sizeof(int)
// 或等价于指针运算：

// c
// (char*)arr + i * N * sizeof(int) + j * sizeof(int)

// 原因：

// 内存布局的确定性：
// 二维数组在内存中是连续存储的，但函数需要知道每行的长度（即 N）才能正确计算元素偏移量。
// 例如，访问 arr[i][j] 时，需通过 i * N + j 定位到具体的内存位置。若省略 N，编译器无法计算行偏移。

// 类型完整性：
// 在C语言中，数组类型包含维度信息。

// int arr[][N] 的实际类型是 “指向 int[N] 的指针”（即 int (*)[N]），每一行是一个完整的 int[N] 数组。

// 若省略 N，类型不完整，编译器无法确定指针步长（一行占多少字节）。

