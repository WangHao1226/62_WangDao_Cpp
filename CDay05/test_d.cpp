// 1. 存储期限的类型
// C 语言中定义了四种存储期限：

// (1) 自动存储期限（Automatic Storage Duration）
// 定义：变量在进入其声明所在的代码块（如函数、循环、条件语句等）时分配内存，退出块时自动释放。

// 适用变量：

// 未使用 static 或 extern 修饰的局部变量（默认行为）。

// 函数参数。

// (2) 静态存储期限（Static Storage Duration）
// 定义：变量在程序启动时分配内存，程序结束时释放，生命周期贯穿整个程序运行期间。

// 适用变量：

// 全局变量（在函数外定义的变量）。

// 用 static 修饰的局部变量。

// (3) 线程存储期限（Thread Storage Duration，C11 起）
// 定义：变量在线程启动时分配内存，线程结束时释放，每个线程拥有独立的实例。

// 适用变量：

// 使用 _Thread_local 或 thread_local（C23 起）修饰的变量。

// (4) 动态存储期限（Allocated Storage Duration）
// 定义：通过 malloc、calloc 等函数手动分配的内存，需显式调用 free 释放。

// 适用对象：

// 动态分配的内存块（如 int *p = malloc(sizeof(int))）。

// 特点：

// 生命周期完全由程序员控制（从分配直到释放）。

// 指针变量本身可能具有自动或静态存储期，但其指向的内存具有动态存储期


