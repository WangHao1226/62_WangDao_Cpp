// © 什么是局部变量，什么是外部变量，它们的作用域分别是什么？
// 1. 局部变量 (Local Variable)
// 定义：
// 在函数内部或代码块内部（如 {} 块、循环、条件语句等）定义的变量。
// 作用域：

// 函数作用域：变量在定义它的函数内可见。

// 块作用域（C99 起）：变量在定义它的代码块内可见（如 {}、for、if 等）。

// 生命周期：
// 局部变量在函数/块执行时创建，在函数/块结束时销毁。
// 每次函数调用会重新初始化（除非使用 static 修饰）。

// 特点：

// 不同函数中的同名局部变量互不影响。

// 未初始化的局部变量值为随机值（取决于内存状态）。




// 2. 外部变量 (External Variable，即全局变量)
// 定义：
// 在函数外部定义的变量，通常称为全局变量。
// 作用域：

// 文件作用域：从定义位置开始，到文件末尾均可访问。

// 跨文件作用域：通过 extern 关键字可在其他文件中访问。
// 生命周期：
// 全局变量在程序启动时创建，在程序结束时销毁，生命周期贯穿整个程序运行期间。

// 特点：

// 未初始化的全局变量默认值为 0（或 NULL）。

// 所有函数均可读写全局变量，可能导致数据耦合问题（需谨慎使用）。

// 可通过 static 关键字限制为当前文件内可见（静态全局变量）：