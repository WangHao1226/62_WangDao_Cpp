// (a) 为什么传递数组时，除了要传入数组名，还要传入长度？


// 在C语言中，传递数组时除了数组名还需要传入长度的原因如下：
// 数组退化为指针：
// 当数组作为函数参数传递时，数组名会退化为指向其首元素的指针。此时，函数内部无法通过sizeof(arr)获取数组的实际长度
// （sizeof仅返回指针的大小，而非数组总字节数）。
// 缺乏元数据：
// C语言的数组是“裸”内存块，没有存储长度等元数据。函数无法自行推断数组的边界，必须依赖额外参数明确长度，以避免越界访问。
// 动态数组的通用性：
// 对于动态分配的数组（如通过malloc创建），其长度在编译时未知，必须由程序员显式记录并传递，确保函数正确处理不同大小的数组。